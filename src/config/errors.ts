export const getMappedError = (errorCode?: string): string | undefined => {
  const translationKey = getTranslationKey(errorCode)
  if (!translationKey) return undefined

  return getLocalizedError(translationKey)
}

const getTranslationKey = (errorCode?: string): string | undefined => {
  if (!errorCode) return undefined

  const exactTranslationKey = exactErrorCodes[errorCode]
  if (exactTranslationKey) return exactTranslationKey

  const splitMessage = errorCode.split('-')
  const suffix = splitMessage.length === 2 ? splitMessage[1] : errorCode
  const commonTranslationKey = commonErrorCodes[suffix]
  if (commonTranslationKey) return commonTranslationKey

  return undefined
}

const getLocalizedError = (translationKey: string): string | undefined => {
  const normalizedTranslationKey = translationKeyAliases[translationKey] ?? translationKey
  const localizedMessage = i18n.t(`error:errors-bff.${normalizedTranslationKey}`, {
    defaultValue: '',
  })

  return localizedMessage || undefined
}
